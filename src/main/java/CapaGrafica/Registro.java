    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
                                
    try {
        String usuario = txtNombre.getText();
        String contrasenia = new String(JPass.getPassword());

        if (usuario.isEmpty() || contrasenia.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos.");
            return;
        }

        // Primero verifico si ya existe el usuario
        boolean existe = fachadaper.iniciarSesion(usuario, contrasenia); 
        if (existe) {
            JOptionPane.showMessageDialog(null, "El usuario ya est√° registrado ‚ùå");
            return;
        }

        // Crear el objeto Login y registrar
        Login nuevo = new Login();
        nuevo.setNombre_usuario(usuario);
        nuevo.setContrasenia(contrasenia);

        fachadaper.registrarUsuario(nuevo);
        JOptionPane.showMessageDialog(null, "Usuario registrado con √©xito ‚úÖ");

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error al registrar: " + ex.getMessage());
    }

    limpiar();
    }//GEN-LAST:event_btnRegistroActionPerformed

    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
       try {
        String usuario = txtNombre.getText();
        String contrasenia = new String(JPass.getPassword());

        if (usuario.isEmpty() || contrasenia.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos.");
            return;
        }

        boolean valido = fachadaper.iniciarSesion(usuario, contrasenia);

        if (valido) {
            JOptionPane.showMessageDialog(null, "Bienvenido " + usuario + " üéâ");
            
            limpiar();
            dispose(); // cierro la ventana actual
            Ingreso in = new Ingreso(); // abro la otra ventana
            in.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrectos ‚ùå");
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error al iniciar sesi√≥n: " + ex.getMessage());
    }
        
    }//GEN-LAST:event_btnIniciarActionPerformed

